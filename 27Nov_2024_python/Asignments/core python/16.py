# 2.Write a Python program to access values using dictionary keys.
# Creating a dictionary
my_dict = {
    "name": "Alice",
    "age": 30,
    "city": "New York",
    "job": "Engineer"
}

# Accessing values using dictionary keys
name = my_dict["name"]  # Accessing value for the key 'name'
age = my_dict["age"]    # Accessing value for the key 'age'
city = my_dict["city"]  # Accessing value for the key 'city'
job = my_dict["job"]    # Accessing value for the key 'job'

# Printing the accessed values
print("Name:", name)
print("Age:", age)
print("City:", city)
print("Job:", job)
